package com.alma42.mapgen.grid.coordinate;

public class ACoordinates {

  private final int x;
  private final int y;

  /**
   * @param x
   * @param y
   */
  public ACoordinates(final int x, final int y) {
    super();
    this.x = x;
    this.y = y;
  }

  /**
   * @return the x
   */
  public int getX() {
    return this.x;
  }

  /**
   * @return the y
   */
  public int getY() {
    return this.y;
  }

}
